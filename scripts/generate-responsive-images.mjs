import fs from 'node:fs/promises';
import path from 'node:path';
import sharp from 'sharp';
import { responsiveImageConfigs } from './responsive-image-config.mjs';

const repoRoot = path.resolve(import.meta.dirname, '..');
const publicDir = path.join(repoRoot, 'apps', 'web', 'public');
const imagesDir = path.join(publicDir, 'images');
const manifestPath = path.join(
  repoRoot,
  'packages',
  'shared',
  'src',
  'responsiveImageAssets.ts',
);

const formatConfigs = [
  {
    extension: 'avif',
    encode: (image, quality) => image.avif({ quality }),
    qualityKey: 'avifQuality',
  },
  {
    extension: 'webp',
    encode: (image, quality) => image.webp({ quality }),
    qualityKey: 'webpQuality',
  },
  {
    extension: 'jpg',
    encode: (image, quality) =>
      image.jpeg({ quality, mozjpeg: true, progressive: true }),
    qualityKey: 'jpegQuality',
  },
];

await fs.mkdir(imagesDir, { recursive: true });
await cleanGeneratedImages();

const generatedAssets = [];

for (const config of responsiveImageConfigs) {
  const sourcePath = path.join(publicDir, config.sourceName);
  const sourceBuffer = await fs.readFile(sourcePath);
  const sourceMetadata = await sharp(sourceBuffer).rotate().metadata();

  if (!sourceMetadata.width || !sourceMetadata.height) {
    throw new Error(
      `Could not read intrinsic dimensions for ${config.sourceName}`,
    );
  }

  const widths = config.widths.filter((width) => width <= sourceMetadata.width);
  if (!widths.includes(config.fallbackWidth)) {
    throw new Error(
      `${config.sourceName} fallback width ${config.fallbackWidth} was not generated`,
    );
  }

  for (const width of widths) {
    const pipeline = sharp(sourceBuffer, { failOn: 'warning' })
      .rotate()
      .resize({
        width,
        withoutEnlargement: true,
      });

    for (const format of formatConfigs) {
      const outputPath = path.join(
        imagesDir,
        `${config.name}-${width}.${format.extension}`,
      );
      const encodedImage = format.encode(
        pipeline.clone(),
        config[format.qualityKey],
      );

      await encodedImage.toFile(outputPath);
    }
  }

  generatedAssets.push({
    ...config,
    width: sourceMetadata.width,
    height: sourceMetadata.height,
    widths,
  });
}

await fs.writeFile(manifestPath, buildManifest(generatedAssets));

async function cleanGeneratedImages() {
  const entries = await fs.readdir(imagesDir, { withFileTypes: true });
  const generatedNamePattern = new RegExp(
    `^(${responsiveImageConfigs
      .map((config) => escapeRegExp(config.name))
      .join('|')})-\\d+\\.(avif|webp|jpg)$`,
  );

  await Promise.all(
    entries
      .filter(
        (entry) => entry.isFile() && generatedNamePattern.test(entry.name),
      )
      .map((entry) => fs.rm(path.join(imagesDir, entry.name))),
  );
}

function buildManifest(assets) {
  const exports = assets
    .map(
      (asset) => `export const ${asset.exportName}: ResponsiveImageAsset = {
  src: ${quote(buildResponsiveImagePath(asset.name, asset.fallbackWidth, 'jpg'))},
  alt: ${quote(asset.alt)},
  objectPosition: ${quote(asset.objectPosition)},
  width: ${asset.width},
  height: ${asset.height},
  fallbackSrc: ${quote(buildResponsiveImagePath(asset.name, asset.fallbackWidth, 'jpg'))},
  avifSrcSet: ${quote(buildSrcSet(asset.name, asset.widths, 'avif'))},
  webpSrcSet: ${quote(buildSrcSet(asset.name, asset.widths, 'webp'))},
  jpegSrcSet: ${quote(buildSrcSet(asset.name, asset.widths, 'jpg'))},
};`,
    )
    .join('\n\n');

  return `// Generated by scripts/generate-responsive-images.mjs. Do not edit by hand.

export interface ResponsiveImageAsset {
  src: string;
  alt: string;
  objectPosition?: string;
  width: number;
  height: number;
  fallbackSrc: string;
  avifSrcSet: string;
  webpSrcSet: string;
  jpegSrcSet: string;
}

${exports}
`;
}

function buildSrcSet(name, widths, extension) {
  return widths
    .map(
      (width) =>
        `${buildResponsiveImagePath(name, width, extension)} ${width}w`,
    )
    .join(', ');
}

function buildResponsiveImagePath(name, width, extension) {
  return `/images/${name}-${width}.${extension}`;
}

function quote(value) {
  return JSON.stringify(value);
}

function escapeRegExp(value) {
  return value.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
}
